#include "all_lib.h"

void	ft_ten_power(int i, char *c)
{
	if (i == 3)
		ft_strstr(c, "1000");
	else if (i == 6)
		ft_strstr(c, "1000000");
	else if (i == 9)
		ft_strstr(c, "1000000000");
	else if (i == 12)
		ft_strstr(c, "1000000000000");
	else if (i == 15)
		ft_strstr(c, "1000000000000000");
	else if (i == 18)
		ft_strstr(c, "1000000000000000000");
	else if (i == 21)
		ft_strstr(c, "1000000000000000000000");
	else if (i == 24)
		ft_strstr(c, "1000000000000000000000000");
	else if (i == 27)
		ft_strstr(c, "1000000000000000000000000000");
	else if (i == 30)
		ft_strstr(c, "1000000000000000000000000000000");
	else if (i == 33)
		ft_strstr(c, "1000000000000000000000000000000000");
	else if (i == 36)
		ft_strstr(c, "1000000000000000000000000000000000000");
}
